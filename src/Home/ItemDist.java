    public ItemDist(Home home, PanelFrame frame, int table, SDN sdn) {
        this();
        this.home = home;
        this.frame = frame;
        this.table = table;
        this.sdn = sdn;
        if (table == TABLE_DISPATCH) {
            tableName = "despatch_note";
            title.setText("Stock Dispatch Note");

        } else {
            tableName = "stock_return_note";
            title.setText("Return Dispatch Note");
        }
        tb.update_table("SELECT\n"
                + "\n"
                + "     item.`item_code` AS 'Item Code',\n"
                + "     item.`description` AS item_description,\n"
                + "     item.`manufacturer` AS item_manufacturer,\n"
                + "  despatch_items.`qty` AS 'Released Qty'\n"
                + "FROM\n"
                + "     `despatch_note` despatch_note INNER JOIN `despatch_items` despatch_items ON despatch_note.`id` = despatch_items.`despatch_note_id`\n"
                + "     INNER JOIN `item` item ON despatch_items.`item_id` = item.`id`\n"
                + "WHERE\n"
                + "     sdn_no = '" + sdn.getSdn_no() + "'", jTable1);
        sdnno.setText(sdn.getSdn_no());
        setSize(home.getSize());
        setLocationRelativeTo(home);
        setVisible(true);
    }

