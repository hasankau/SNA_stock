    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         
        String sdnn = sdnno.getText().trim(),
                item_co = item_code.getText().trim(),
                qt = qty.getText().trim();
        
        boolean b = false;

            home.setProcess("Saving stock return note");
            b = db.insertIntoSrnDistItems(sdnn, item_co, qt);
            if (b) {
                
                tb.update_table("select * from item_dist", jTable1);
                
                home.setProcess("Item saved");
                
            } else {
                home.setProcess("Error occured");
                JOptionPane.showMessageDialog(null, "Could not be saved", "Error", JOptionPane.ERROR_MESSAGE);
            }
        
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            if (jTable1.getSelectedRows().length > 0) {
                sdn.setId(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString().trim());
                sdn.setDate(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString().trim());
                sdn.setSdn_no(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString().trim());
                if (table == TABLE_DISPATCH) {
                    ((Return) frame).copySupplier(sdn);
                }
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Please select a row");
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
            if (jTable1.getSelectedRows().length > 0) {
                sdn.setId(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString().trim());
                sdn.setDate(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString().trim());
                sdn.setSdn_no(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString().trim());
                if (table == TABLE_DISPATCH) {
                    ((Return) frame).copySupplier(sdn);
                } else {
                    //((Dispatch)frame).copySupplier(sdn);
                }

                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Please select a row");
            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

